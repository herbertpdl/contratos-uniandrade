    <p class="clausule-description">
     <strong> <u>CLÁUSULA 1</u></strong> - <strong>DO ESTÁGIO e FORMALIZAÇÃO</strong>: O presente <em>TERMO DE COMPROMISSO DE ESTÁGIO (<strong>TCE</strong>)</em> não poderá ser manuscrito. O TCE tem por objetivo formalizar as condições básicas para a
      realização de <em>ESTÁGIO DE ESTUDANTES</em> da <em>INSTITUIÇÃO DE ENSINO</em> junto à <em>UNIDADE CONCEDENTE</em>, entendendo-se estágio como ato educativo escolar supervisionado, desenvolvido no ambiente de trabalho,
      que visa à preparação para o trabalho produtivo de educandos que estejam devidamente matriculados e frequentando o ensino regular em instituições de educação superior.
    </p>

    <p class="clausule-description">
      <strong><u>CLÁUSULA 2</u></strong> - <strong>DA ADEQUAÇÃO DO ESTÁGIO À PROPOSTA PEDAGÓGICA</strong>: O estágio faz parte do projeto pedagógico do curso, além de integrar o itinerário formativo do ESTAGIÁRIO, visando ao aprendizado
      de competências próprias da atividade profissional e à contextualização curricular, objetivando o desenvolvimento do educando para a vida cidadã e para o trabalho. Será desenvolvido no 7° período,
      conforme proposta pedagógica do curso e a etapa da formação escolar.<br/>
       <strong>Paragrafo único</strong> - O desenvolvimento do estágio conforme previsto no caput da presente cláusula e, desde que atendidos os requisitos da legislação vigente, não cria vínculo empregatício de qualquer natureza.
    </p>

    <p class="clausule-description">
      <strong><u>CLÁUSULA 3</u></strong> - <strong>DO OBJETO DO ESTÁGIO</strong>: As atividades principais a serem desenvolvidas pelo(a)  ESTAGIÁRIO(A), compatíveis com o Curso ao qual se refere, são aquelas pertinentes ao profissional de NUTRIÇÃO,
      sendo basicamente: (AQUI SÃO AS ATIVIDADES QUE VOU TER QUE SEPARAR DE CADA MODALIDADE DE ESTÁGIO), que mantenham relação com aspectos pertinentes à profissão do NUTRICIONISTA , podendo ser ampliadas,
      reduzidas, alteradas ou substituídas, de acordo com a progressividade do estágio e do Currículo, sempre dentro do Contexto Básico da Profissão.
    </p>

    <p class="clausule-description">
      <strong><u>CLÁUSULA 4</u></strong> - <strong>DO RELATÓRIO DE ATIVIDADES DO(A) ESTAGIÁRIO(A)</strong>: O (A) <em>ESTAGIÁRIO (A)</em> obriga-se desde já a apresentar ao professor orientador do estágio, Prof.° XXXXXXXXXX relatório de atividades com
      indicação resumida das atividades desenvolvidas, em prazo não superior a 6 (seis) meses.
    </p>

    <p class="clausule-description">
      <strong><u>CLÁUSULA 5</u></strong> - <strong>DO RELATÓRIO DE ATIVIDADES DA UNIDADE CONCEDENTE</strong>: A <em>UNIDADE CONCEDENTE</em>, na pessoa do Supervisor de Estágio, Sr(a). XXXXXXXXX deverá enviar ao professor orientador do estágio, com periodicidade
      mínima de 6 (seis) meses, relatório de atividades, com vista obrigatória ao estagiário.
    </p>

    <p class="clausule-description">
      <strong><u>CLÁUSULA 6</u></strong> - <strong>DO SEGURO</strong>:  O seguro contra acidentes pessoais será contratado em favor do(a) ESTAGIÁRIO(A), a cargo da instituição de ensino, na forma do inciso IV do art. 9º da Lei 11.788/2008.
      Apólice 0982.00.07428.032-5 Porto Seguro.
    </p>

    <p class="clausule-description">
      <strong>CLÁUSULA 7</strong> - <strong>DA JORNADA DAS ATIVIDADES</strong>: A jornada de atividade do(a) ESTÁGIARIO(A) será desenvolvida no horário compreendido entre 08:00 h às 14:00 h (de Segunda-feira, Quarta-feira e
      Sexta-feira), totalizando 18  horas semanais. Não podendo exceder, em qualquer hipótese, as 6 (seis) horas diárias e 30 (trinta) horas semanais, exceto quando o estágio se relacionar a
      cursos que alternam teoria e prática, nos períodos em que não estão programadas aulas presenciais, quando a jornada poderá ser de até 40 (quarenta) horas semanais na forma da lei.<br/>
      <strong>Parágrafo único</strong> – Em caso de estágio curricular, as atividades devem ser cumpridas de forma integral, abonando-se apenas as faltas em caso de atestados de doenças infecto contagiosa,
      óbito de parente próximo (pais, irmãos avós, cônjuges ou filhos), compromissos eleitorais e/ou judiciais mediante comprovação. As faltas por hospitalizações serão justificadas mediante
      comprovante protocolado na Central de Relacionamento até no prazo máximo de 48 horas e ocorrerá análise da Coordenação de Estágio/Curso ou pelo colegiado de curso, levando-se em consideração
      os procedimentos realizados. A presença no estágio é 100% obrigatória.
    </p>

    <p class="clausule-description">
      <strong>CLÁUSULA 8</strong> - <strong>DA REDUÇÃO DA JORNADA DE ATIVIDADES</strong>: A carga horária do(a) estagiário(a), nos dias de avaliação, será reduzida de  zero horas. Esta redução não poderá ser inferior a cinquenta por
      cento da jornada diária para garantir o bom desempenho do(a) estagiário(a) em suas verificações de aprendizagem.<br/>
      <strong>Parágrafo único</strong> – Caso as provas sejam aplicadas fora da data especificada no calendário acadêmico, o(a) ESTAGIÁRIO(A) deverá informar, por escrito, à UNIDADE CONCEDENTE,
      com antecedência mínima de 3 (três) dias, a data da realização das provas.
    </p>

    <p class="clausule-description">
      <strong>CLÁUSULA 9</strong> - DO RECESSO DAS ATIVIDADES: É assegurado ao(a) estagiário(a), sempre que o estágio tenha duração igual ou superior a 1 (um) ano, período de recesso de 30 (trinta) dias, a ser gozado
      preferencialmente durante suas férias escolares.<br/>
      <strong>Parágrafo Primeiro</strong> - O recesso de que trata esta cláusula deverá ser remunerado quando o estagiário receber bolsa ou outra forma de contraprestação.<br/>
      <strong>Parágrafo Segundo</strong> - Os dias de recesso previstos nesta cláusula serão concedidos de maneira proporcional, caso o estágio tenha duração inferior a 1 (um) ano.
    </p>

    <p class="clausule-description">
      CLÁUSULA 10 − DA BOLSA AUXÍLIO E DOS BENEFÍCIOS: O(A) ESTAGIÁRIO(A) ora acordado poderá ser remunerado e, neste caso, o(a) ESTUDANTE receberá uma bolsa mensal fixa e não reajustável no
      valor de R$   xxxxxxxxxxx   , podendo, a exclusivo critério da UNIDADE CONCEDENTE, receber benefícios relacionados a transporte, alimentação e saúde, entre outros, não caracterizando vínculo
      empregatício, nos termos do art. 12 da Lei 11.788/2008, os quais  não caracterizam vinculo empregatício.
    </p>

    <p class="clausule-description">
      CLÁUSULA 11 - DA VIGENCIA: O presente TCE terá vigência de   00 de fevereiro de 2019 à 00 de junho de 2019, podendo ser denunciado a qualquer tempo, unilateralmente, mediante comunicação
      escrita ou ser prorrogado através da emissão de um TERMO ADITIVO, sendo a soma dos Termos Aditivos não poderá ser superior a 2 (dois) anos, na forma do Art. 12 da Lei 11.788/2008.
    </p>

    <p class="clausule-description">
      CLÁUSULA 12 − DO CANCELAMENTO: O presente Termo de Compromisso de Estágio será rescindido nas seguintes hipóteses:
      a) a pedido do(a) estagiário(a);
      b) por conclusão ou por interrupção do Curso na IES;
      c) pela ausência de matrícula regular na IES;
      d) quando da violação pelo(a) estagiário(a) de obrigação prevista no “Termo de Compromisso”;
      e) pelo não comparecimento ao estágio, sem motivo justificado, por oito dias consecutivos, ou quinze dias intercalados;
      f) por comportamento funcional ou social do(a) estagiário(a) incompatível com as normas do CONCEDENTE;
      g) por interesse da Administração da CONCEDENTE, ou em atendimento a qualquer dispositivo de ordem legal ou regulamentar.
      Parágrafo único – Entende-se por conclusão de Curso a que se refere à alínea “b” supra, a aprovação do(a) aluno(a)-estagiário(a) no último período do Curso, sem qualquer dependência,
      exaurindo assim o currículo exigido para sua formação profissional, e por interrupção qualquer ato ou fato de iniciativa do estudante do CENTRO UNIVERSITÁRIO CAMPOS DE ANDRADE, ou mesmo decorrente
      de circunstância que implique em solução de continuidade do Curso do(a) aluno(a)-estagiário(a), impondo o cancelamento ou suspensão do estágio.
    </p>

    <p class="clausule-description">
      CLÁUSULA 13 - DA FORMALIZAÇÃO DO DESLIGAMENTO: Ocorrendo qualquer das hipóteses previstas nas alíneas “a”, “d”, “e”, “f” e “g”, da cláusula anterior, a CONCEDENTE comunicará por escrito o
      CENTRO UNIVERSITÁRIO CAMPOS DE ANDRADE, que, por sua vez, também se obriga igualmente a comunicar por escrito à CONCEDENTE o desligamento do aluno do Curso, seja qual for o motivo, inclusive por
      conclusão do mesmo.
    </p>

    <p class="clausule-descriptio">
      CLÁUSULA 14 – DO FORO: As partes elegem o foro da Comarca de Curitiba/PR, para dirimir quaisquer questões relativas ao presente instrumento, em detrimento de qualquer outro, por mais privilegiado que seja.
      E por estarem de comum acordo com as condições deste Termo de Compromisso de Estágio, as partes assinam (3) três de igual teor.
